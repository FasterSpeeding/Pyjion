using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace Pyjionlib.Test
{

    [TestClass]
    public class TestPlaneOperations
    {
        [TestMethod]
        public void TestVectorDot()
        {
            PlaneOperations.ProductArgs args = new PlaneOperations.ProductArgs() { 1.0f, 2.0f, 3.0f, 4.0f, 5.0f, 6.0f, 7.0f, 8.0f };
            Assert.AreEqual(70.0f, PlaneOperations.VectorDotProduct());
        }

        [TestMethod]
        public void TestVectorDotAsDelegate()
        {
            PlaneOperations.VectorDotProductDelegate del = PlaneOperations.VectorDotProduct;
            Assert.AreEqual(70.0f, del(1.0f, 2.0f, 3.0f, 4.0f, 5.0f, 6.0f, 7.0f, 8.0f));
        }
    }
}
